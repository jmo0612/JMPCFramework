    private void closing(){
        if(this.ok){
            if(this.okCancelRunnables!=null){
                if(this.okCancelRunnables.size()>0){
                    this.okCancelRunnables.get(0).run();
                }
            }
        }else{
            if(this.okCancelRunnables!=null){
                if(this.okCancelRunnables.size()>1){
                    this.okCancelRunnables.get(1).run();
                }
            }
        }
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
    }

