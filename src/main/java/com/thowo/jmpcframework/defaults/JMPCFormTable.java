    @Override
    public void init(JMFormTableList tableList) {
        this.setTitle(tableList.getTitle());
        this.jLabel1.setText(tableList.getTitle());
        this.search=JMPCFilterBox.create(jPanel6, tableList.getFieldProp().getLabel("PROMPT_SEARCH"), tableList.getFieldProp().getLabel("FILTER_INDICATOR"));
        this.jPanel4.removeAll();
        this.jPanel4.setLayout(new BorderLayout());
        this.jPanel4.add((Component) tableList.getBtnGroup().getEditorPanel(),BorderLayout.WEST);
        this.jPanel4.add((Component) tableList.getBtnGroup().getNavigationPanel(),BorderLayout.EAST);
        
        JScrollPane sp=new JScrollPane((Component) tableList.getDBList());
        this.jPanel3.removeAll();
        this.jPanel3.setLayout(new BorderLayout());
        this.jPanel3.add(sp, BorderLayout.CENTER);
        this.search.setFilterAction(tableList.getFilterAction());
    }

